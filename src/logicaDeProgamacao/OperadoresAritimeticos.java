package logicaDeProgamacao;

public class OperadoresAritimeticos {

	public static void main(String[] args) {
        int numero = 10;
        System.out.println("Número = " + numero);
        System.out.println("Soma = " + numero + " + 5 | Soma = " + (numero + 5));
        System.out.println("subtração= " + numero + " - 5 | Subtração = " + (numero - 5));
        System.out.println("Multiplicação = " + numero + " * 5 | Multiplicação = " + (numero * 5));
        System.out.println("Divisão = " + numero + " / 5 | Divisão = " + (numero / 5));
        System.out.println("Resto = " + numero + " % 5 | Resto = " + (numero % 5));
	}

}
